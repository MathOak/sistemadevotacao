package com.suam.bean;

public class Mesario {
	
	private String titulo_mesario;
	private String nome_mesario;
	private String zona_eleitoral;
	private String senha;
	
	public String getTitulo_mesario() {
		return titulo_mesario;
	}

	public void setTitulo_mesario(String titulo_mesario) {
		this.titulo_mesario =  titulo_mesario;
	}
	
	public String getNome_mesario() {
		return nome_mesario;
	}

	public void setNome_mesario(String nome_mesario) {
		this.nome_mesario = nome_mesario;
	}
	
	public String getZona_eleitoral() {
		return zona_eleitoral;
	}

	public void setZona_eleitoral(String zona_eleitoral) {
		this.zona_eleitoral = zona_eleitoral;
	}
	
	public String getSenha() {
		return senha;
	}

	public void setSenha(String senha) {
		this.senha = senha;
	}

}
